//Math Object
let x : number =  Math.round(10.7) //round off
console.log(x); //11

let y : number = Math.ceil(12.3) // full round off like top value
console.log(y); //13

let z : number = Math.floor(12.9) //minimum round off
console.log(z); //12

let a : number = Math.trunc(12.34785) //first value only rest of value after decimal will be drop
console.log(a); //12

let b : number = Math.pow(2, 4) // base and power 
console.log(b); //16

let c : number = Math.sqrt(4) //square root
console.log(c); // 2

let d : number = Math.min(10,3,45,67,2) //gives minimum number
console.log(d); // 2

let e : number = Math.max(100,34,340,80) //gives maximum number
console.log(e); // 340
